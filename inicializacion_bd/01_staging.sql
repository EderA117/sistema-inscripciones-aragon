-- Inicialización de la Base de Datos de Catálogos (db_staging)
CREATE SCHEMA IF NOT EXISTS staging;
SET search_path TO staging, public;

-- 1. Estructura de Tablas
CREATE TABLE staging."PlanEstudio" (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre character varying,
    estado character varying
);

CREATE TABLE staging."Profesor" (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    num_empleado character varying UNIQUE,
    nombre character varying,
    apellidos character varying
);

CREATE TABLE staging."Asignatura" (
    cve character varying PRIMARY KEY,
    nombre character varying,
    creditos integer,
    plan_estudio_id integer REFERENCES staging."PlanEstudio"(id) DEFERRABLE
);

